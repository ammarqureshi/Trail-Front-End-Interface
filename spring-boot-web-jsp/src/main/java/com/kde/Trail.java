package com.kde;

public class Trail {

	private String district;
	private String name;
	private String length;
	private boolean dogsAllowed;
	private String quality;
	private String climbHeight;
	private String startLatLong;
	private String endLatLong;
	private String completionTime;
	private String startPoint;


	public Trail() {
	}


	public void setDistrict(String district) {
		this.district = district;
	}
	public void setName(String name) {
		this.name = name;
	}
	public void setLength(String length) {
		this.length = length;
	}
	public void setDogsAllowed(boolean dogsAllowed) {
		this.dogsAllowed = dogsAllowed;
	}
	public void setQuality(String quality) {
		this.quality = quality;
	}
	public void setClimbHeight(String climbHeight) {
		this.climbHeight = climbHeight;
	}
	public void setStartLatLong(String startLatLong) {
		this.startLatLong = startLatLong;
	}
	public void setEndLatLong(String endLatLong) {
		this.endLatLong = endLatLong;
	}
	public void setCompletionTime(String completionTime) {
		this.completionTime = completionTime;
	}
	public void setStartPoint(String startPoint) {
		this.startPoint = startPoint;
	}


	public String getDistrict() {
		return district;
	}


	public String getName() {
		return name;
	}


	public String getLength() {
		return length;
	}


	public boolean getDogsAllowed() {
		return dogsAllowed;
	}


	public String getQuality() {
		return quality;
	}


	public String getClimbHeight() {
		return climbHeight;
	}


	public String getStartLatLong() {
		return startLatLong;
	}


	public String getEndLatLong() {
		return endLatLong;
	}


	public String getCompletionTime() {
		return completionTime;
	}


	public String getStartPoint() {
		return startPoint;
	}
	
	
	

}
